# write a program to Sum of numbers from 1 to n

sum=0
num=int(input('enter number: '))
for i in range(1,num+1):
    sum=sum+i
print("Sum of numbers: ",sum)



# write a program to Product of numbers from 1 to n

product=1
num=int(input('enter number: '))
for i in range(1,num+1):
    product=product*i
print("Product of numbers:",product)